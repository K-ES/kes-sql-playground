-- База данных (Database)  
Организованная система хранения и управления данными.

-- Система управления базами данных (DBMS)  
Программное обеспечение для создания, хранения и управления базами данных.

-- SQL (Structured Query Language)  
Язык запросов для работы с реляционными базами данных.

-- Типы баз данных  
Базы данных делятся на несколько основных типов в зависимости от модели хранения данных.

-- Реляционные базы данных (Relational databases)  
Данные хранятся в таблицах со строками и столбцами. Используется SQL.  
Примеры: PostgreSQL, MySQL, Oracle, SQL Server.

-- Документо-ориентированные базы (Document databases)  
Данные хранятся в документах (обычно JSON).  
Пример: MongoDB.

-- Key-Value базы  
Данные хранятся как пара ключ-значение.  
Пример: Redis.

-- Графовые базы  
Данные представлены узлами и связями между ними.  
Пример: Neo4j.

-- Колончатые базы (Column stores)  
Данные физически хранятся по колонкам, а не по строкам.  
Пример: ClickHouse.

-- Таблица (Table)  
Структура хранения данных в реляционной базе.

-- Строка / запись (Row / Record)  
Одна запись данных в таблице реляционной базы.

-- Столбец (Column)  
Поле таблицы, описывающее один тип данных.

-- Первичный ключ (Primary Key)  
Уникальный идентификатор строки таблицы.

-- Внешний ключ (Foreign Key)  
Поле, которое ссылается на ключ другой таблицы.

-- Индекс (Index)  
Структура для ускорения поиска данных. Увеличивает время вставки и обновления данных, но уменьшает время поиска.

-- Представление (View)  
Виртуальная таблица, основанная на SQL-запросе.

-- Запрос (Query)  
Команда для получения или изменения данных.

-- SELECT  
Оператор SQL для чтения данных.

-- WHERE  
Фильтрация строк по условию.

-- JOIN  
Объединение данных из нескольких таблиц.

-- GROUP BY  
Группировка строк для агрегатных вычислений.

-- HAVING  
Фильтрация сгруппированных данных.

-- ORDER BY  
Сортировка результата.

-- Подзапрос (Subquery)  
Запрос внутри другого запроса.

-- Агрегатные функции  
Функции, работающие с группой строк (COUNT, SUM, AVG).

-- Транзакция (Transaction)  
Набор операций, выполняемых как единое целое.

-- ACID  
Свойства транзакций: атомарность, согласованность, изоляция, долговечность.

-- Атомарность (Atomicity)  
Транзакция выполняется полностью или не выполняется совсем.

-- Согласованность (Consistency)  
После завершения транзакции база данных остается в корректном состоянии и все ограничения соблюдаются.

-- Изоляция (Isolation)  
Параллельные транзакции не должны влиять друг на друга.

-- Долговечность (Durability)  
После фиксации транзакции изменения сохраняются даже при сбое системы.