008 Операции с set-множествами

Set-операции работают с результатами SELECT как с множествами строк.

Форма:
SELECT ...
<operator>
SELECT ...

Операции:

UNION
Объединение без дубликатов.

UNION ALL
Объединение с дубликатами. Быстрее.

INTERSECT
Пересечение (общие строки).

INTERSECT ALL (PostgreSQL)
Пересечение с учётом количества строк.

EXCEPT
Разность (A − B).

EXCEPT ALL (PostgreSQL)
Разность с учётом количества строк.

Правила:
- одинаковое число колонок
- совместимые типы
- порядок колонок важен
- DISTINCT по умолчанию (кроме ALL)

NULL:
В set-операциях NULL считается равным NULL.

JOIN объединяет колонки.
Set-операции объединяют строки.